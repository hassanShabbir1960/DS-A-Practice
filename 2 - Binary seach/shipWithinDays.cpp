bool isPossible(vector<int>& weights, int days,int mid){
   
   int temp =0;
   int daycount=1;
   for (int i =0;i<weights.size();i++){
        if (temp + weights[i] <= mid){
            temp+= weights[i];
            
        
        }else {
            daycount++;
            temp =weights[i];
        }

        
        if (daycount > days || weights[i] > mid){
            return false;
        }
   }
    return true;
   
}

int shipWithinDays(vector<int>& weights, int days) {
    int low = 1;
    int high = 0;
    
    for (auto& n : weights) high += n;

    int mid;
    int sol =-1;

    while (low<=high){
        mid = low + (high -low)/2;

        if (isPossible(weights,days,mid)){
            sol = mid;
            high = mid -1;
        }
        else{
            low = mid +1;
        }
    }

    return sol;


}


